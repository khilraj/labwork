#volume of cubid
length = int(input("enter length of the cubid in cm: "))
breadth = int(input("enter breadth of the cubid in cm: "))
height = int(input("enter height of the cubid in cm: "))
volume_of_cubid = length * breadth * height
print(volume_of_cubid)
print(f"so the volume of cubid is {volume_of_cubid}cm.")
